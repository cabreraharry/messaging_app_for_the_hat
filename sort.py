def quadrant(x, y):
    if x > 0 and y >= 0:
        return 0
    
    elif x <= 0 and y > -x:
        return 1
    
    elif y > 0 and y <= -x:
        return 2
    
    elif x < 0 and y <= 0:
        return 3
    
    elif x >= 0 and y < -x:
        return 4
    
    elif y < 0 and y >= -x:
        return 5
    
def rotation(x,y,z):
    rotation_list = [3,4,5,0,1,2]
    z = (z + rotation_list[quadrant(x,y)]) % 6
    return (x,y,z)


