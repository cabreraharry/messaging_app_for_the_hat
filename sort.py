def generatelayer(layer):
    initial = [(0, 0)]
    if layer == 0:
        return initial

    axis = [(layer,0),(0,layer),(-layer,layer),(-layer,0),(0,-layer),(layer,-layer)]
    if layer == 1:
        return axis

    between = []
    adjustments = [(-1, 1), (-1, 0), (0, -1), (1, -1), (1, 0), (0, 1)]
    
    for i in range(6):
        coord = [axis[i][0],axis[i][1]]
        coord_next = [axis[(i+1)%6][0],axis[(i+1)%6][1]]      

        while coord != coord_next:
            between.append((coord[0],coord[1]))
            coord = [coord[0]+adjustments[i][0],coord[1]+adjustments[i][1]]
                
    return between


