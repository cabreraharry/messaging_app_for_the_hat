def sort_data(data):
    # Precompute the position map
    position_map = {value: index for index, value in enumerate(BASIS_FOR_10_LAYERS)}

    # Function to get the smallest position of any element in a sublist
    def get_position(sublist):
        return min(position_map.get(element, float('inf')) for element in sublist)

    # Sort data based on the precomputed positions
    sorted_data = sorted(data, key=get_position)

    return sorted_data

